<h3>j4age</h3>
j4age has it's roots within the popular statistics tool JoomlaStats. j4age was originally planned to be releases as the new JoomlaStats Version 4.0.0. Unfortunately not all developers could imagine the advantages of the planned release, which has caused an split up of the JS Team.

<h3>History</h3>
<p>The original JoomlaStats component had essential design issues and many despaired approaches to handle issues have left their scars. The original target to gather valuable information got somehow lost; JoomaStats became more an gimmick for private webpages rather than a tool to add some real analytical value for administrators.

As result the component JoomlaStats was  almost completely rewritten and cleaned-up. All core elements such as browser &amp; system determination, counting process, database and reports haven been replaced by more advanced &amp; faster algorithms. The overall look &amp; feel and page structure was kept similar to it's ancestors, but many improvements were done.
</p>

<h3>Short Facts</h3>
<ul>
<li>up to 50% more accurate statistic data for evaluating visitor information</li>
<li>more efficient way of storing information in the database</li>
<li>increased amount of visit details information without increasing the DB</li>
<li>Own Installer implementation to avoid DB timeouts for huge Dbs and weak machines</li>
<li>No more risk to corrupt the Database through failures within the migration process between versions upgrades</li>
<li>Component improved to private users as well as for business to extract more valuable information for search engine (SEF/SEO) optimisations, collecting potential leaks and issues within the web-page</li>
<li>Support to handle huge amounts of statistic data, without complete failure of the whole component</li>
<li>an caching mechanism was added to dramatically improve the response time of modules and backend</li>
<li>the summarisation feature is no more supported as the new caching mechanism is used to preserve reports</li>
</ul>

<h3>Good to know</h3>

